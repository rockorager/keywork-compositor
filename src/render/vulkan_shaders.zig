fn decodedSize(comptime encoded: []const u8) usize {
    return std.base64.standard.Decoder.calcSizeForSlice(encoded) catch unreachable;
}

fn decodeSpirv(comptime encoded: []const u8) [decodedSize(encoded) / @sizeOf(u32)]u32 {
    @setEvalBranchQuota(10_000);
    const byte_count = decodedSize(encoded);
    comptime std.debug.assert(byte_count % @sizeOf(u32) == 0);
    var bytes: [byte_count]u8 = undefined;
    _ = std.base64.standard.Decoder.decode(&bytes, encoded) catch unreachable;
    var words: [byte_count / @sizeOf(u32)]u32 = undefined;
    for (&words, 0..) |*word, index| {
        word.* = std.mem.readInt(u32, bytes[index * 4 ..][0..4], .little);
    }
    return words;
}
