const std = @import("std");

pub const quad_source =
    \\#version 450
    \\layout(push_constant) uniform Push {
    \\    vec2 target_size;
    \\    vec2 texture_size;
    \\    float swap_rb;
    \\} pc;
    \\layout(location=0) in vec4 in_dest;
    \\layout(location=1) in vec4 in_source;
    \\layout(location=2) in vec4 in_clip;
    \\layout(location=3) in vec4 in_color;
    \\layout(location=4) in vec4 in_rounded;
    \\layout(location=5) in vec4 in_parameters;
    \\layout(location=0) out vec2 pixel;
    \\layout(location=1) flat out vec4 dest;
    \\layout(location=2) flat out vec4 source;
    \\layout(location=3) flat out vec4 color;
    \\layout(location=4) flat out vec4 rounded;
    \\layout(location=5) flat out vec4 parameters;
    \\void main() {
    \\    vec2 q=vec2(float(gl_VertexIndex&1),float((gl_VertexIndex>>1)&1));
    \\    vec2 pos=in_clip.xy+q*in_clip.zw;
    \\    gl_Position=vec4(pos/pc.target_size*2.0-1.0,0,1);
    \\    pixel=pos;
    \\    dest=in_dest;
    \\    source=in_source;
    \\    color=in_color;
    \\    rounded=in_rounded;
    \\    parameters=in_parameters;
    \\}
;

pub const solid_source =
    \\#version 450
    \\layout(push_constant) uniform Push {
    \\    vec2 target_size;
    \\    vec2 texture_size;
    \\    float swap_rb;
    \\} pc;
    \\layout(location=3) flat in vec4 color;
    \\layout(location=0) out vec4 out_color;
    \\void main() { out_color=pc.swap_rb>0.5 ? color.bgra : color; }
;

pub const image_source =
    \\#version 450
    \\layout(set=0,binding=0) uniform sampler2D tex;
    \\layout(push_constant) uniform Push {
    \\    vec2 target_size;
    \\    vec2 texture_size;
    \\    float swap_rb;
    \\} pc;
    \\layout(location=0) in vec2 pixel;
    \\layout(location=1) flat in vec4 dest;
    \\layout(location=2) flat in vec4 source;
    \\layout(location=4) flat in vec4 rounded;
    \\layout(location=5) flat in vec4 parameters;
    \\layout(location=0) out vec4 out_color;
    \\void main() {
    \\    vec2 q=(pixel-dest.xy)/dest.zw;
    \\    vec2 uv=(source.xy+q*source.zw)/pc.texture_size;
    \\    vec4 color=texture(tex,uv);
    \\    if (pc.swap_rb>0.5) color=color.bgra;
    \\    if (parameters.x>0.0) {
    \\        vec2 center=clamp(pixel,rounded.xy+vec2(parameters.x),rounded.xy+rounded.zw-vec2(parameters.x));
    \\        float coverage=clamp(parameters.x+0.5-distance(pixel,center),0.0,1.0);
    \\        color*=coverage;
    \\    }
    \\    out_color=pc.swap_rb>0.5 ? color.bgra : color;
    \\}
;

pub const shadow_source =
    \\#version 450
    \\layout(push_constant) uniform Push {
    \\    vec2 target_size;
    \\    vec2 texture_size;
    \\    float swap_rb;
    \\} pc;
    \\layout(location=0) in vec2 pixel;
    \\layout(location=3) flat in vec4 color;
    \\layout(location=4) flat in vec4 rounded;
    \\layout(location=5) flat in vec4 parameters;
    \\layout(location=0) out vec4 out_color;
    \\void main() {
    \\    vec2 q=abs(pixel-(rounded.xy+rounded.zw*0.5))-(rounded.zw*0.5-vec2(parameters.x));
    \\    float distance=length(max(q,vec2(0)))+min(max(q.x,q.y),0.0)-parameters.x;
    \\    float coverage=parameters.y>0.0 ? clamp(0.5-distance/(2.0*parameters.y),0.0,1.0) : clamp(0.5-distance,0.0,1.0);
    \\    vec4 shaded=color*coverage;
    \\    out_color=pc.swap_rb>0.5 ? shaded.bgra : shaded;
    \\}
;

